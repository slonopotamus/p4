int
CharSetCvt8859_1toUTF8::Cvt(const char **sourcestart, const char *sourceend,
	       char **targetstart, char *targetend)
{
	unsigned int v;

	while (*sourcestart < sourceend && *targetstart < targetend)
	{
	    v = **sourcestart & 0xff;
	    if (v & 0x80)
	    {
		if (1 + *targetstart == targetend)
		{
		    lasterr = PARTIALCHAR;
		    break;
		}
		if (v >= 0xc0)
		{
		    **targetstart = 0xc3;
		    v -= 0x40;
		}
		else
		    **targetstart = 0xc2;
		*++*targetstart = v;
	    }
	    else
		**targetstart = v;
	    ++charcnt;
	    if( v == '\n' ) {
		++linecnt;
		charcnt = 0;
	    }
	    ++*targetstart;
	    ++*sourcestart;
	}
	return 0;
}

